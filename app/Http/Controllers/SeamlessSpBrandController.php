    public function delete(Request $request)
    {

       
        $data = json_encode(array(
            "doSendDataCustomerApply" => array(   
                "TRANSACTION_CODE"=>"DEL_SP_BRAND",
                "P_LANGUAGE"=>"IN",
                "P_GUID"=>$request->Id,
            ),
        ));
        //dd($data);
         //API GET
        //$url = "https://acc-dev1.outsystemsenterprise.com/ACCWorldCMS/rest/UserCMSAPI/GetAllUserCMS?RoleId=".$session[0]["RoleId"]."&SubMenuId=".$session[0]["SubMenuId"]; 
        $url = config('global.base_url_sofia').'/seamless/accone/customerapply';
        // $url = "http://172.16.4.32:8301/restV2/acccash/getdata/transactionapply";
        $ch = curl_init($url);                   
        curl_setopt($ch, CURLOPT_POST, true);                                  
        curl_setopt($ch, CURLOPT_POSTFIELDS, $data);
        curl_setopt($ch, CURLOPT_SSL_VERIFYPEER, FALSE);   
        curl_setopt($ch, CURLOPT_HTTPHEADER, array('Content-Type:application/json'));                                                             
        curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);                                                                  
        $result = curl_exec($ch);
        $err = curl_error($ch);
        curl_close($ch);
        $Hasils= json_decode($result); 
        //   dd($Hasils);

      
        //dd($Hasils);
        if ($Hasils->OUT_STAT == "T"){
            return redirect("seamless-spbrand")->with('success','Cabang berhasil terhapus');
        }else{
            return redirect("seamless-spbrand")->with('error',$Hasils->OUT_MESS);
        }
       
    }


